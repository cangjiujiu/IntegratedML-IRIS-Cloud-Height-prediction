package com.yss.entity;


public class FamilyMsg {

  private long id;
  private double family;
  private double father;
  private double mother;
  private long csex;
  private double height;
  private long num;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public double getFamily() {
    return family;
  }

  public void setFamily(double family) {
    this.family = family;
  }


  public double getFather() {
    return father;
  }

  public void setFather(double father) {
    this.father = father;
  }


  public double getMother() {
    return mother;
  }

  public void setMother(double mother) {
    this.mother = mother;
  }


  public long getCsex() {
    return csex;
  }

  public void setCsex(long csex) {
    this.csex = csex;
  }


  public double getHeight() {
    return height;
  }

  public void setHeight(double height) {
    this.height = height;
  }


  public long getNum() {
    return num;
  }

  public void setNum(long num) {
    this.num = num;
  }

}
